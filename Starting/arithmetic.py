x = 2

x += 3  # equivalent to x = x + 3
# In this example, we are using the += operator to add 3 to the current value of x. This is equivalent to writing x = x + 3. After this operation, the value of x will be updated to 5.
print(x)  # Output: 5

x -= 1  # equivalent to x = x - 1
# Here, we are using the -= operator to subtract 1 from the current value of x. This is equivalent to writing x = x - 1. After this operation, the value of x will be updated to 4.
print(x)  # Output: 4

x *= 2  # equivalent to x = x * 2
# In this case, we are using the *= operator to multiply the current value of x by 2. This is equivalent to writing x = x * 2. After this operation, the value of x will be updated to 8.
print(x)  # Output: 8

x /= 4  # equivalent to x = x / 4
# Here, we are using the /= operator to divide the current value of x by 4. This is equivalent to writing x = x / 4. After this operation, the value of x will be updated to 2.0 (since division in Python results in a float).
print(x)  # Output: 2.0

x //= 2  # equivalent to x = x // 2
# In this example, we are using the //= operator to perform floor division on the current value of x by 2. This is equivalent to writing x = x // 2. After this operation, the value of x will be updated to 1.0 (since floor division of 2.0 by 2 results in 1.0).
print(x)  # Output: 1.0

x %= 2  # equivalent to x = x % 2
# Here, we are using the %= operator to calculate the modulus of the current value of x by 2. This is equivalent to writing x = x % 2. After this operation, the value of x will be updated to 1.0 (since the remainder when 1.0 is divided by 2 is 1.0).
print(x)  # Output: 1.0

x **= 3  # equivalent to x = x ** 3
# In this case, we are using the **= operator to perform exponentiation on the current value of x by raising it to the power of 3. This is equivalent to writing x = x ** 3. After this operation, the value of x will be updated to 1.0 (since 1.0 raised to any power is still 1.0).
print(x)  # Output: 1.0


